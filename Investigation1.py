class نظام_المعرفة_الاستخباراتي_الشامل:
    """النظام المتكامل الذي يربط كل المكونات"""
    
    def __init__(self):
        self.بنك_المعرفة = بنك_المعرفة_الاستخباراتي()
        self.محرك_العمليات = محرك_العمليات(self.بنك_المعرفة)
        self.المحلل = محلل_استخباراتي(self.محرك_العمليات)
        self.تقارير: List[Dict] = []
    
    def استعلام_معرفي(self, مصطلح: str) -> Optional[بصمة_معرفية]:
        """البحث عن مصطلح في بنك المعرفة"""
        for مفهوم in self.بنك_المعرفة.المفردات.values():
            if مصطلح in مفهوم.الاسم or مصطلح in مفهوم.التصنيفات:
                return مفهوم
        for أسلوب in self.بنك_المعرفة.الأساليب.values():
            if مصطلح in أسلوب.الاسم or مصطلح in أسلوب.التصنيفات:
                return أسلوب
        for نظرية in self.بنك_المعرفة.النظريات.values():
            if مصطلح in نظرية.الاسم or مصطلح in نظرية.التصنيفات:
                return نظرية
        return None
    
    def استعلام_عبر_الزمن(self, زمن: الزمن) -> List[بصمة_معرفية]:
        """استخراج كل المفاهيم المرتبطة بزمن معين"""
        النتائج = []
        for م in self.بنك_المعرفة.المفردات.values():
            if م.الزمن == زمن:
                النتائج.append(م)
        for أ in self.بنك_المعرفة.الأساليب.values():
            if أ.الزمن == زمن:
                النتائج.append(أ)
        return النتائج
    
    def استعلام_احتمالي(self, عتبة: float) -> List[بصمة_معرفية]:
        """استخراج المفاهيم ذات احتمالية وجود أكبر من عتبة"""
        النتائج = []
        for م in self.بنك_المعرفة.المفردات.values():
            if م.احتمالية_الوجود >= عتبة:
                النتائج.append(م)
        return النتائج
    
    def عرض_آليات_العمل(self, مفهوم: بصمة_معرفية) -> List[Dict]:
        """عرض كل آليات العمل المرتبطة بمفهوم"""
        آليات = []
        for آلية in مفهوم.آليات_العمل:
            آليات.append({
                "المبدأ_العلمي": آلية.المبدأ_العلمي,
                "الخطوات": آلية.الخطوات_التنفيذية,
                "الأدوات": آلية.الأدوات_المستخدمة,
                "معدل_النجاح": آلية.معدل_النجاح,
                "العوامل_المؤثرة": آلية.العوامل_المؤثرة
            })
        return آليات
    
    def توليد_تقرير_شامل(self) -> Dict:
        """توليد تقرير شامل عن حالة المعرفة الاستخباراتية"""
        التقرير = {
            "الإحصاءات": {
                "المفاهيم": len(self.بنك_المعرفة.المفردات),
                "المصادر": len(self.بنك_المعرفة.المصادر),
                "الأساليب": len(self.بنك_المعرفة.الأساليب),
                "النظريات": len(self.بنك_المعرفة.النظريات),
                "البيئات": len(self.بنك_المعرفة.البيئات)
            },
            "تحليل_زمني": {
                زمن.name: len(self.استعلام_عبر_الزمن(زمن))
                for زمن in الزمن
            },
            "جاهزية_المستقبل": {
                "الأساليب_المستقبلية": len([
                    أ for أ in self.بنك_المعرفة.الأساليب.values()
                    if أ.الزمن in [الزمن.مستقبل_قريب, الزمن.مستقبل_بعيد, الزمن.حاضر_ناشئ]
                ])
            }
        }
        self.تقارير.append(التقرير)
        return التقرير
    
    def تصدير_المعرفة(self, صيغة: str = "json") -> str:
        """تصدير كل المعرفة إلى صيغة قابلة للنقل"""
        if صيغة == "json":
            البيانات = {
                "المفاهيم": {k: {
                    "الاسم": v.الاسم,
                    "التصنيفات": v.التصنيفات,
                    "الزمن": v.الزمن.name,
                    "آليات_العمل": [a.__dict__ for a in v.آليات_العمل]
                } for k, v in self.بنك_المعرفة.المفردات.items()},
                "الأساليب": {k: {
                    "الاسم": v.الاسم,
                    "التصنيفات": v.التصنيفات,
                    "الزمن": v.الزمن.name,
                    "آليات_العمل": [a.__dict__ for a in v.آليات_العمل]
                } for k, v in self.بنك_المعرفة.الأساليب.items()}
            }
            return json.dumps(البيانات, indent=2, default=str, ensure_ascii=False)
        return ""

