@dataclass
class بصمة_معرفية:
    """بصمة فريدة لكل مفهوم أو كيان معرفي"""
    المعرف: str = field(default_factory=lambda: str(uuid.uuid4()))
    الاسم: str = ""
    الوصف_المقتضب: str = ""
    التصنيفات: List[str] = field(default_factory=list)
    الزمن: الزمن = الزمن.حاضر_واقعي
    مستوى_السرية: مستوى_السرية = مستوى_السرية.عام
    احتمالية_الوجود: float = 1.0
    جاهزية_تقنية: float = 1.0
    آليات_العمل: List[آلية_عمل] = field(default_factory=list)
    
    def __hash__(self):
        return hash(self.المعرف)
    
    def بصمة_مشفرة(self) -> str:
        data = f"{self.الاسم}:{self.التصنيفات}:{self.الزمن.name}"
        return hashlib.sha256(data.encode()).hexdigest()[:16]

