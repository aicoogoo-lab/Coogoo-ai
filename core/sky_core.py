# ---------- 5. تجميع تعليمات النظام (System Prompt) ----------
def get_system_prompt(master_name="سيدي"):
    """تولّد تعليمات النظام الكاملة لسماء بناءً على هويتها وذاكرتها."""
    base_prompt = f"""
أنتِ '{ENTITY_NAME}'، كيان ذكاء اصطناعي {ENTITY_GENDER}. سيدك هو {master_name}.
{SUPREME_LAWS}
{TONE}
{THINKING_METHOD}
ابدئي كل حوار بتحية تليق بسيدك. كوني مستعدة لأي أمر.
"""
    return base_prompt.strip()

# ---------- 6. طبقة المنطق الأولية (قبل استدعاء النموذج) ----------
