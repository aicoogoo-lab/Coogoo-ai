@dataclass
class وحدة_معرفية:
    """وحدة أساسية للمعرفة المنظمة"""
    المعرف: str = field(default_factory=lambda: str(uuid.uuid4()))
    العنوان: str = ""
    الملخص: str = ""
    التصنيفات: List[str] = field(default_factory=list)
    الفترة: الفترة_البحثية = الفترة_البحثية.معاصر
    مستوى_الاطلاع: مستوى_الوصول = مستوى_الوصول.مفتوح
    احتمالية_التحقق: float = 1.0
    جاهزية_البحث: float = 1.0
    آليات_البحث: List[آلية_بحث] = field(default_factory=list)
    
    def __hash__(self):
        return hash(self.المعرف)
    
    def ترميز_معرفي(self) -> str:
        data = f"{self.العنوان}:{self.التصنيفات}:{self.الفترة.name}"
        return hashlib.sha256(data.encode()).hexdigest()[:16]

