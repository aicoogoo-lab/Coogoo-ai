class نظام_المعرفة_المتكامل:
    """النظام المتكامل لإدارة المعرفة البحثية"""
    
    def __init__(self):
        self.المكتبة = مكتبة_المعرفة()
        self.نظام_البحث = نظام_إدارة_البحث(self.المكتبة)
        self.المحلل = محلل_بحثي(self.نظام_البحث)
        self.تقارير: List[Dict] = []
    
    def استعلام_عن_مصطلح(self, مصطلح: str) -> Optional[وحدة_معرفية]:
        """البحث عن مصطلح في المكتبة"""
        for وحدة in self.المكتبة.الوحدات_المعرفية.values():
            if مصطلح in وحدة.العنوان or مصطلح in وحدة.التصنيفات:
                return وحدة
        for أسلوب in self.المكتبة.الأساليب.values():
            if مصطلح in أسلوب.العنوان or مصطلح in أسلوب.التصنيفات:
                return أسلوب
        for إطار in self.المكتبة.الأطر.values():
            if مصطلح in إطار.العنوان or مصطلح in إطار.التصنيفات:
                return إطار
        return None
    
    def استعلام_حسب_الفترة(self, فترة: الفترة_البحثية) -> List[وحدة_معرفية]:
        """استخراج كل الوحدات المرتبطة بفترة معينة"""
        النتائج = []
        for وحدة in self.المكتبة.الوحدات_المعرفية.values():
            if وحدة.الفترة == فترة:
                النتائج.append(وحدة)
        for أسلوب in self.المكتبة.الأساليب.values():
            if أسلوب.الفترة == فترة:
                النتائج.append(أسلوب)
        return النتائج
    
    def عرض_آليات_البحث(self, وحدة: وحدة_معرفية) -> List[Dict]:
        """عرض كل آليات البحث المرتبطة بوحدة معرفية"""
        آليات = []
        for آلية in وحدة.آليات_البحث:
            آليات.append({
                "الأساس_العلمي": آلية.الأساس_العلمي,
                "الخطوات": آلية.الخطوات_المنهجية,
                "الأدوات": آلية.الأدوات,
                "نسبة_التحقق": آلية.نسبة_التحقق,
                "المتغيرات_المؤثرة": آلية.المتغيرات_المؤثرة
            })
        return آليات
    
    def توليد_تقرير_شامل(self) -> Dict:
        """توليد تقرير شامل عن حالة المعرفة"""
        التقرير = {
            "إحصاءات": {
                "الوحدات_المعرفية": len(self.المكتبة.الوحدات_المعرفية),
                "الخبراء": len(self.المكتبة.الخبراء),
                "الأساليب": len(self.المكتبة.الأساليب),
                "الأطر": len(self.المكتبة.الأطر),
                "البيئات": len(self.المكتبة.البيئات)
            },
            "تحليل_زمني": {
                فترة.name: len(self.استعلام_حسب_الفترة(فترة))
                for فترة in الفترة_البحثية
            },
            "جاهزية_المستقبل": {
                "الأساليب_المستقبلية": len([
                    أ for أ in self.المكتبة.الأساليب.values()
                    if أ.الفترة in [الفترة_البحثية.قريب_المدى, الفترة_البحثية.بعيد_المدى, الفترة_البحثية.انتقالي]
                ])
            }
        }
        self.تقارير.append(التقرير)
        return التقرير

