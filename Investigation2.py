@dataclass
class بيئة_بحث:
    """البيئة أو المجال محل الدراسة والبحث"""
    الاسم: str = ""
    النوع: str = ""
    الإحداثيات: Dict[str, float] = field(default_factory=dict)
    الخصائص: Dict[str, Any] = field(default_factory=dict)
    مستوى_الصعوبة: float = 0.0
    إمكانية_الدراسة: float = 1.0
    فترة_الدراسة: الفترة_البحثية = الفترة_البحثية.معاصر
    
    def تقييم_التحديات(self) -> float:
        return self.مستوى_الصعوبة * (1 - self.إمكانية_الدراسة)

